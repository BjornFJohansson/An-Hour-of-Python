def translate(DNA):
    excess=0
    if len(DNA)/3.0 != int(len(DNA)/3.0):
        excess=len(DNA)%3
        DNA=DNA[:-excess]

    protein=""
    for a in range(0, len(DNA), 3):
        DNA=DNA.upper()
        codon=DNA[a:a+3].replace("T","U")
        protein=protein+codontable[codon]
    return protein + "?"*excess

